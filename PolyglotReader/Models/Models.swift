// MARK: - PDF Document Metadata
struct PDFDocumentMetadata: Codable, Identifiable {
    let id: String
    var name: String
    var size: Int
    var uploadedAt: Date
    var storagePath: String
    var thumbnailData: Data?
    var summary: String?

    // Klasör ve Etiket Sistemi
    var folderId: UUID?
    var tags: [Tag]
    var aiCategory: String?

    // Favori ve Okuma İlerlemesi
    var isFavorite: Bool
    var pageCount: Int?         // files.page_count — eski kayıtlarda nil, lazy backfill
    var lastReadPage: Int?      // reading_progress.page
    var lastOpenedAt: Date?     // reading_progress.updated_at → "Son Açılan" sıralaması
    var deletedAt: Date?        // Soft delete: dolu ise dosya çöp kutusunda

    /// 0...1 arası okuma ilerlemesi; sayfa sayısı bilinmiyorsa nil.
    var readingProgress: Double? {
        guard let pageCount, pageCount > 0, let lastReadPage else { return nil }
        return min(1.0, Double(lastReadPage) / Double(pageCount))
    }

    var formattedSize: String {
        let formatter = ByteCountFormatter()
        formatter.allowedUnits = [.useKB, .useMB, .useGB]
        formatter.countStyle = .file
        return formatter.string(fromByteCount: Int64(size))
    }

    var formattedDate: String {
        let formatter = DateFormatter()
        formatter.dateStyle = .medium
        formatter.locale = Locale(identifier: "tr_TR")
        return formatter.string(from: uploadedAt)
    }

    init(
        id: String,
        name: String,
        size: Int,
        uploadedAt: Date,
        storagePath: String,
        thumbnailData: Data? = nil,
        summary: String? = nil,
        folderId: UUID? = nil,
        tags: [Tag] = [],
        aiCategory: String? = nil,
        isFavorite: Bool = false,
        pageCount: Int? = nil,
        lastReadPage: Int? = nil,
        lastOpenedAt: Date? = nil,
        deletedAt: Date? = nil
    ) {
        self.id = id
        self.name = name
        self.size = size
        self.uploadedAt = uploadedAt
        self.storagePath = storagePath
        self.thumbnailData = thumbnailData
        self.summary = summary
        self.folderId = folderId
        self.tags = tags
        self.aiCategory = aiCategory
        self.isFavorite = isFavorite
        self.pageCount = pageCount
        self.lastReadPage = lastReadPage
        self.lastOpenedAt = lastOpenedAt
        self.deletedAt = deletedAt
    }
}

// MARK: - Annotation
enum AnnotationType: String, Codable {
    case highlight
    case underline
    case strikethrough
}

/// One annotation rectangle as stored in Supabase (`annotations.data.rects`).
///
/// Canonical format — all NEW annotations from both iOS and web:
/// - Percentages (0-100) of the page as displayed, i.e. the cropBox with the
///   page rotation applied (what pdf.js renders), top-left origin.
///
/// Legacy format — old iOS annotations (values typically > 100):
/// - Raw PDFKit page-space points, bottom-left origin.
///
/// Read paths on both platforms distinguish the formats via the
/// "all values <= 100 → percentage" heuristic; conversion math lives in
/// `AnnotationCoordinateConverter`.
struct AnnotationRect: Codable {
    var x: CGFloat
    var y: CGFloat
    var width: CGFloat
    var height: CGFloat

    /// CGFloat değerini güvenli hale getirir (NaN, Infinity → 0)
    private static func sanitize(_ value: CGFloat) -> CGFloat {
        guard value.isFinite else { return 0 }
        return value
    }

    /// Geçerli bir rect mi kontrol eder
    var isValid: Bool {
        x.isFinite && y.isFinite && width.isFinite && height.isFinite &&
        width > 0 && height > 0
    }

    /// Debug amaçlı validasyon raporu
    var validationReport: String {
        var issues: [String] = []
        if !x.isFinite { issues.append("x=\(x)") }
        if !y.isFinite { issues.append("y=\(y)") }
        if !width.isFinite { issues.append("width=\(width)") }
        if !height.isFinite { issues.append("height=\(height)") }
        if width <= 0 { issues.append("width<=0") }
        if height <= 0 { issues.append("height<=0") }

        return issues.isEmpty ? "✅ Valid" : "❌ Issues: \(issues.joined(separator: ", "))"
    }

    // Custom Codable - NaN/Infinity değerlerini 0'a çevir
    enum CodingKeys: String, CodingKey {
        case x, y, width, height
    }

    init(x: CGFloat, y: CGFloat, width: CGFloat, height: CGFloat) {
        self.x = Self.sanitize(x)
        self.y = Self.sanitize(y)
        self.width = Self.sanitize(width)
        self.height = Self.sanitize(height)
    }

    init(from decoder: Decoder) throws {
        let container = try decoder.container(keyedBy: CodingKeys.self)
        x = Self.sanitize(try container.decode(CGFloat.self, forKey: .x))
        y = Self.sanitize(try container.decode(CGFloat.self, forKey: .y))
        width = Self.sanitize(try container.decode(CGFloat.self, forKey: .width))
        height = Self.sanitize(try container.decode(CGFloat.self, forKey: .height))
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.container(keyedBy: CodingKeys.self)
        try container.encode(Self.sanitize(x), forKey: .x)
        try container.encode(Self.sanitize(y), forKey: .y)
        try container.encode(Self.sanitize(width), forKey: .width)
        try container.encode(Self.sanitize(height), forKey: .height)
    }
}

