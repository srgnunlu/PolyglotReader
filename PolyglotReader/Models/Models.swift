    enum MessageRole: String, Codable {
        case user
        case model
    }

    init(
        id: String = UUID().uuidString,
        role: MessageRole,
        text: String,
        timestamp: Date = Date(),
        isError: Bool? = nil
    ) {
        self.id = id
        self.role = role
        self.text = text
        self.timestamp = timestamp
        self.isError = isError
    }
}

